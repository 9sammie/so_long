#include "libft.h"

size_t	ft_strlcpy(char *dest, const char *src, size_t size)
{
	size_t	result;
	size_t	i;

	i = 0;
	result = ft_strlen((char *)src);
	if (size <= 0)
		return (ft_strlen((char *)src));
	while (src[i] && i < size - 1)
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (result);
}
